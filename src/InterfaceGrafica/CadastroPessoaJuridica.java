    public void CadastraPessoaJuridica() {
        this.conectar.conectaBanco();
        
        pessoaJuridica.setNome(campoNome.getText());
        pessoaJuridica.setEndereco(campoEndereco.getText());
        pessoaJuridica.setBairro(campoBairro.getText());
        pessoaJuridica.setCep(campoCep.getText());
        pessoaJuridica.setCidade(campoCidade.getText());
        pessoaJuridica.setEstado(campoEstado.getText());
        pessoaJuridica.setEmail(campoEmail.getText());
        pessoaJuridica.setDdd(Integer.valueOf(campoDdd.getText()));
        pessoaJuridica.setTelefone(Integer.valueOf(campoTelefone.getText()));
        pessoaJuridica.setCnpj(Integer.valueOf(campoCnpj.getText()));
        pessoaJuridica.setTipo(campoTipo.getText());
        
        try {
            this.conectar.insertSQL("INSERT INTO dadosjuridica ("
                    + "nome,"
                    + "endereco,"
                    + "bairro,"
                    + "cep,"
                    + "cidade,"
                    + "estado,"
                    + "email,"
                    + "ddd,"
                    + "telefone,"
                    + "cnpj,"
                    + "tipo"
                    + ") VALUES ("
                    + "'" + pessoaJuridica.getNome() + "',"
                    + "'" + pessoaJuridica.getEndereco() + "',"
                    + "'" + pessoaJuridica.getBairro() + "',"
                    + "'" + pessoaJuridica.getCep() + "',"
                    + "'" + pessoaJuridica.getCidade() + "',"
                    + "'" + pessoaJuridica.getEstado() + "',"
                    + "'" + pessoaJuridica.getEmail() + "',"
                    + "'" + pessoaJuridica.getDdd() + "',"
                    + "'" + pessoaJuridica.getTelefone() + "',"
                    + "'" + pessoaJuridica.getCnpj()+ "',"
                    + "'" + pessoaJuridica.getTipo() + "'"
                    + ");");
        } catch (Exception e) {
            System.out.println("Erro ao cadastrar cliente" + e.getMessage());
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar cliente");
        } finally {
            
            this.conectar.fechaBanco();

        }
        
    }
    
